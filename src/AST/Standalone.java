package AST;

public class Standalone {
    IsBoolean isboolean;

    public IsBoolean isIsboolean() {
        return isboolean;
    }

    public void setIsboolean(IsBoolean isboolean) {
        this.isboolean = isboolean;
    }

    @Override
    public String toString() {
        return "\nStandalone{" +
                "\n" + isboolean +
                "\n}";
    }
}
